/**
  * @brief  Function implementing the serialUI_task thread.
  * @param  argument: Not used
  * @retval None
  */
extern const SER_cmdStruct cmdStructTab[N_CMD];
uint32_t SerialUI_TASKDELAY = 500;
/* USER CODE END Header_TASK_serialUI */
void TASK_serialUI(void *argument)
{
  /* init code for USB_DEVICE */
  MX_USB_DEVICE_Init();
  /* USER CODE BEGIN 5 */
  uint32_t cmd;
  char args[64];

  osDelay(1000);
  SER_clear();
  _printf("\r\n                    ####################\r\n");
  _printf("                    ## RTOS DSP v%d.%02d ##\r\n", MAJ_VERSION, MIN_VERSION);
  _printf("                    ####################\r\n\n");

  /* Infinite loop */
  _printd("Serial UI task started.\r\n");
  for(;;)
  {
		/* Check for serial command */
	  	if((cmd = SER_getCmd(cmdStructTab, N_CMD, args)) != N_CMD)
	  	{
	  		cmdStructTab[cmd].ExecFunction(args);
	  	}
	  	SER_flush();
		HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_13);
		osDelay(SerialUI_TASKDELAY);
  }

  // Clean Task
  _printd("/!\\ Killing TASK_serialUI !\r\n");
  osThreadTerminate(NULL);

  /* USER CODE END 5 */
}

